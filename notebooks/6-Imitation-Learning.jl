# ‚ïî‚ïê‚ï° fcd6e17c-3fe7-4983-b257-e3bf2b9a3635
md"""
## Behavior Cloning
Using supervised learning, _behavior cloning_ attempts to learn a policy by mimicing actions provided by a teacher (or _expert demonstrations_)‚Äîmeaning we have a target we want to learn, which we get from the demonstrations.$^1$
"""

# ‚ïî‚ïê‚ï° 624f8110-2c09-497b-bfc2-eb1e004dabb6
md"""
A continuous neural network policy is trained to match the actions from the demonstrations. 
"""

# ‚ïî‚ïê‚ï° 3dfb2eb4-f49d-45b5-bfd5-5f5a799d27f0
function œÄ_network()
	layer1 = Dense(2, 64, relu, init=Flux.orthogonal)
	layer2 = Dense(64, 64, relu, init=Flux.orthogonal)
	layer3 = Dense(64, 1, tanh, init=Flux.orthogonal)
	layer4 = x -> 2f0*x
	return ContinuousNetwork(Chain(layer1, layer2, layer3, layer4), 1)
end

# ‚ïî‚ïê‚ï° 96e5ff37-537a-426e-a1be-4bc1d6428b56
md"""
Solve for an approximately optimal policy.
"""

# ‚ïî‚ïê‚ï° 8d55c1b8-c27b-4956-a6c8-9d8052a8ee99
solver_bc = BC(œÄ=œÄ_network(), ùíü_demo=expert_trajectories, S=ùíÆ, log=(period=100,));

# ‚ïî‚ïê‚ï° ee476d91-054f-4279-b3c0-c063801724ad
